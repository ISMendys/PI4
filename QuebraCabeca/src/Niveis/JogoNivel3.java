        private void iniciarCintagem() {
        ActionListener action = new ActionListener() {  
            public void actionPerformed(ActionEvent e) {  
                currentSegundo--;
                
                if(currentSegundo<=0){
                    currentMinuto--;
                    currentSegundo = 60;
                }
                if(currentMinuto<0){
                    reinicia();
                    stopTime();
                    timer.restart();
                }
                
                if(currentSegundo<=20 && currentMinuto == 0){
                    label.setBackground(Color.red);
                    label.setForeground(Color.red);
                }else{
                    label.setBackground(Color.white);
                    label.setForeground(Color.white);
                }
                
                String min = currentMinuto <= 9? "0"+currentMinuto:currentMinuto+"";
                String seg = currentSegundo <= 9? "0"+currentSegundo:currentSegundo+"";
                
                label.setText(min+":"+seg);  
            }  
        };  
        this.timer = new Timer(velocidade, action);  
        this.timer.start();
    }

    private void stopTime() {
        timer.stop();
        currentMinuto = 4;
        currentSegundo = 0;
        label.setText("04:00");
    }
    
