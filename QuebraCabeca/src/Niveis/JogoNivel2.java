    private void vereficaRecorde(){
       int minuto = currentMinuto * 60;
       int segundos = currentSegundo;
       int rec = 90-(minuto + segundos);
       jogadorAtual.settempo2(90-rec);
       Recorde.recorde.novoRecord2(rec, jogadorAtual.getUsuario());
       if(Recorde.recorde.getRecordeNivel2()>0){
           setTimer();
        }
    }
    
